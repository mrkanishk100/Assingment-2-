a = []                #Taking a empty list to store elements.

for i in range(1,51): #Making 'for' loop for counting of 1 to 50.
    a.append(i)       #Adding counting 1 to 50 one by one in empty list 'a'.

b = sum(a)            #Summation of elements which stored in list 'a'.

print(f'The sum of numbers from 1 to 50 is: {b}')

